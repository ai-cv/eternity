def get_html(link):
    keep_request = True
    connect_time = 0
    while keep_request:
        UA = random.choice(agent_list) 
        try:
            r = requests.get(link, 
                        headers= {'Host':'iguba.eastmoney.com', 'User-Agent': UA}, 
                        timeout= 10)
            time.sleep(0.5)
            keep_request = False
            return r.text
        except:
            if connect_time == 5:
                print("获取%s失败,已经达到最大重连次数5次" % link)
                break
                #print("获取%s失败,重新连接" % link)
            connect_time += 1
            time.sleep(1)


